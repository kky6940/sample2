package com.ezen.koh2;

public class InsaDTO {
	int id;
	String spot;
	int fpay,pay, totpay;
	public InsaDTO() {}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getSpot() {
		return spot;
	}
	public void setSpot(String spot) {
		this.spot = spot;
	}
	public int getFpay() {
		return fpay;
	}
	public void setFpay(int fpay) {
		this.fpay = fpay;
	}
	public int getPay() {
		return pay;
	}
	public void setPay(int pay) {
		this.pay = pay;
	}
	public int getTotpay() {
		return totpay;
	}
	public void setTotpay(int totpay) {
		this.totpay = totpay;
	}
	
}
